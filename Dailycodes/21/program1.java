import java.util.*;

class ScannerDemo{
	public static void main(String[] args){

		Scanner sc= new Scanner(System.in);

		System.out.println("Enter  the value of name:");
		String str= sc.next();
		System.out.println(str);
		
		System.out.println("Enter  the value of collage:");
		String clg= sc.next();
		System.out.println(clg);

		System.out.println("Enter  the value of ID:");
		String id= sc.next();
		System.out.println(id);

		System.out.println("Enter  the value of CGPI:");
		String Cgpi= sc.next();
		System.out.println(Cgpi);
	}
}


